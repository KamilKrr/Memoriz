        <div class="content">
            <ul>
                <li>
                    <h2 class="subtitle">TXT-Datei</h2>
                    <p>
                        Eigene Sets können als Textdatei erstellt werden. Dabei muss sich die Datei an folgende Regeln halten:
                    </p>
                    <p>
                        <code>Memory-Karte1|Memory-Karte2|Beschreibung(Optional)</code>
                        </br></br>
                        Diese Zeile stellt ein Memory-Paar dar. Um ein funktionierendes Memoryset zu Erstellen braucht man <b>mindestes 8</b> solcher Zeilen (8 Paare => 16 Karten)
                    </p>
                    <p>
                        Es ist möglich <b>extern gehostete Bilder</b> über einen Link hinzuzufügen. Dies erfolgt, indem man, statt einer Memory-Karte, den folgenden Code einträgt:
                    </p>
                    <p>
                        <code>(Beschreibung des Bildes)[Link zum Bild]</code>
                        </br></br>
                        Beispiel:
                        </br></br>
                        <code>(Flagge der Republik Österreich)[https://upload.wikimedia.org/wikipedia/commons/thumb/4/41/Flag_of_Austria.svg/1280px-Flag_of_Austria.svg.png]</code>
                        </br></br>
                    </p>
                </li>
                <li>
                    <h2 class="subtitle">CSV-Datei</h2>
                    <p>
                        Es ist möglich MemorySets direkt als CSV Datei hochzuladen, dabei gelten die <b>selben Regeln</b> wie bei TXT Dateien.
                    </p>
                    <p>
                        Der Unterschied besteht darin, dass der Character <code>|</code> mit <code>;</code> ersetzt wird. Dies ermöglicht es Memorysets direkt in Excel zu Erstellen, dabei sind die <strong>erste</strong> und <strong>zweite Spalte</strong> jeweils Karten die <strong>ein Paar</strong> bilden. Die dritte Spalte, die auch leergelassen werden kann, ist die Beschreibung des Paares.
                    </p>
                </li>
            </ul>
        </div>
</section>